public class ForCounter {
    
    //main method
    public static void main(String[] args) {
        
        //counter intialized with 1, the loop continuing until the 
        //counter less than or euqal to 10, counter increment by 1 
        for (int counter=1;counter<=10;counter++)
            System.out.println(counter);
    }
}
